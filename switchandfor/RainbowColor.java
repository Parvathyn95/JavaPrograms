class RainbowColor{
	public static void main(String args[]){
		String color="Red";
		switch(color){
			case "Violet":
			System.out.println("Component of Rainbow");
			break;
			case "Indigo":
			System.out.println("Component of Rainbow");
			break;
			case "Blue":
			System.out.println("Component of Rainbow");
			break;
			case "Green":
			System.out.println("Component of Rainbow");
			break;
			case "Yellow":
			System.out.println("Component of Rainbow");
			break;
			case "Orange":
			System.out.println("Component of Rainbow");
			break;
			case "Red":
			System.out.println("Component of Rainbow");
			break;
			default:
			System.out.println("Not a Component of Rainbow");
			break;
			}
	}
}